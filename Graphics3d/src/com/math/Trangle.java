package com.math;

public class Trangle {
	private Vec4 pt0;
	private Vec4 pt1;
	private Vec4 pt2;
	
	public Trangle(Vec4 p0, Vec4 p1, Vec4 p2) {
		this.pt0 = p0;
		this.pt1 = p1;
		this.pt2 = p2;
	}
	
	public Vec4 getPt0() {
		return pt0;
	}
	public void setPt0(Vec4 pt0) {
		this.pt0 = pt0;
	}
	public Vec4 getPt1() {
		return pt1;
	}
	public void setPt1(Vec4 pt1) {
		this.pt1 = pt1;
	}
	public Vec4 getPt2() {
		return pt2;
	}
	public void setPt2(Vec4 pt2) {
		this.pt2 = pt2;
	}
}


















